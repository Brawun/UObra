    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            if (txtRFC.getText().isBlank()
                && txtNombre.getText().isBlank()
                && txtAnioNacimiento.getText().isBlank()) {
                JOptionPane.showMessageDialog(null, "Error, llene campos de búsqueda.");
            } else if (!txtRFC.getText().isBlank()
                && txtNombre.getText().isBlank()
                && txtAnioNacimiento.getText().isBlank()) {
                cargarTablaPersonasRFC();
            } else if (txtRFC.getText().isBlank()
                && !txtNombre.getText().isBlank()
                && txtAnioNacimiento.getText().isBlank()) {
                cargarTablaPersonasNombre();
            } else if (txtRFC.getText().isBlank()
                && txtNombre.getText().isBlank()
                && !txtAnioNacimiento.getText().isBlank()) {
                cargarTablaPersonasAnio();
            } else if (!txtRFC.getText().isBlank()
                && !txtNombre.getText().isBlank()
                && !txtAnioNacimiento.getText().isBlank()) {
                cargarTablaPersonasRFC();
            } else if (!txtRFC.getText().isBlank()
                && txtNombre.getText().isBlank()
                && !txtAnioNacimiento.getText().isBlank()) {
                cargarTablaPersonasRFC();
            } else if (!txtRFC.getText().isBlank()
                && !txtNombre.getText().isBlank()
                && txtAnioNacimiento.getText().isBlank()) {
                cargarTablaPersonasRFC();
            } else if (txtRFC.getText().isBlank()
                && !txtNombre.getText().isBlank()
                && !txtAnioNacimiento.getText().isBlank()) {
                cargarTablaPersonasNombreYAnio();
            } else {
                cargarTablaPersonasRFC();
            }
        } catch (Exception e) {
            Logger.getLogger(Historial.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

